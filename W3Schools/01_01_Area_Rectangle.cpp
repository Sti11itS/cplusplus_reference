#include <iostream>
using namespace std;

int main() {
    // Create integer variables
    int length = 4;
    int width = 6;
    int area;

    // Calculate area of rectangle
    area = length * width;

    // Print the variables
    cout << "Length is: " << length << "\n";
    cout << "Width is: " << width << endl;
    cout << "Area of rectangle is: " << area << "\n";

    return 0;
}

